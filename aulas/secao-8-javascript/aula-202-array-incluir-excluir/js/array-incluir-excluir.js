var lista_frutas = ['Banana', 'Maçã']

console.log(lista_frutas)

// Incluir elemento no fim do array
lista_frutas.push('Uva')

// Incluir elemento no início do array
lista_frutas.unshift('Morango')

console.log(lista_frutas)

// Excluir elemento no fim do array
lista_frutas.pop('')

// Excluir elemento no início do array
lista_frutas.shift('')

console.log(lista_frutas)

