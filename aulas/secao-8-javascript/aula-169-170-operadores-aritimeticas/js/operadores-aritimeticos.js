var num1 = parseInt(prompt('Digite o valor de num1'))
var num2 = parseInt(prompt('Digite o valor de num2'))

document.write('A soma entre ' + num1 + ' e ' + num2 + ' é: ' + (num1 + num2) + '<br>')

document.write('A subtração entre ' + num1 + ' e ' + num2 + ' é: ' + (num1 - num2) + '<br>')

document.write('A múltiplicação entre ' + num1 + ' e ' + num2 + ' é: ' + (num1 * num2) + '<br>')

document.write('A divisão entre ' + num1 + ' e ' + num2 + ' é: ' + (num1 / num2) + '<br>')

document.write('A módulo (resto da divisão) entre ' + num1 + ' e ' + num2 + ' é: ' + (num1 % num2) + '<br>')

document.write('A incremento (anterior) de ' + num1 + ' é: ' + (++num1) + '<br>')
document.write('A incremento (posterior) de ' + num1 + ' é: ' + (num1++) + '->' + num1 + '<br>')

document.write('A decremento (anterior) de ' + num1 + ' é: ' + (--num1) + '<br>')
document.write('A decremento (posterior) de ' + num1 + ' é: ' + (num1--) + '->' + num1 + '<br>')